"""
Operator aritmatika pada python sama halnya dengan bahasa pemrograman lain, yaitu operator yang digunakan
untuk melakukan operasi matematika pada nilai numerik.

Operator aritmatika pada python:
** pangkat, contoh 2**3 = 8
* perkalian, contoh 2*3 = 6
/ pembagian, contoh 6/3 = 2
// pembagian bulat, contoh 6//4 = 1
% sisa bagi, contoh 6%4 = 2
+ penjumlahan, contoh 2+3 = 5
- pengurangan, contoh 5-2 = 3

"""

